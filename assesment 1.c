#include <stdio.h>
int main()
{
   int i,a,b,c,sum=0;

   printf("Input Value of number : ");
   scanf("%d, %d, %d",&a ,&b, &c);

   for(i=1;i==a,b,c;i)
   {
     printf("%d ",i);
     sum=i*i*i;
   }
   printf("\nThe Sum of natural numbers upto %d terms : %d \n",a,b,c,sum);
   return 0;

}
